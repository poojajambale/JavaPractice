package com.string;

public class SubString {

	public static void main(String[] args) {
		String s = "Pooja";
		
		System.out.println(s.substring(0, 3));
		System.out.println(s.substring(1));
		
		System.out.println(s.replace('o', 'q'));
		System.out.println(s.replace("Pooja","pooja"));
		
		 	
	}

}
